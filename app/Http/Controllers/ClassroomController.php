     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $classroom= new Classroom();
        $classroom->aula_lunes = $request->lunes;
        $classroom->aula_martes = $request->martes;
        $classroom->aula_miercoles = $request->miercoles;
        $classroom->aula_jueves = $request->jueves;
        $classroom->aula_viernes = $request->viernes;

        $classroom->save();

        return 'La nueva aulas ha sido establecida con exito';
    }

